ALTER TABLE care_plan ADD CONSTRAINT ck_care_plan_schedule_v2 CHECK (
    (schedule_kind = 'ONE_TIME' AND calendar_unit IS NULL AND interval_count IS NULL
        AND fixed_interval_seconds IS NULL AND daily_times IS NULL AND repetitions IS NULL AND end_at_instant IS NULL)
 OR (schedule_kind = 'CALENDAR_INTERVAL' AND calendar_unit IN ('DAY', 'WEEK', 'MONTH', 'YEAR')
        AND interval_count > 0 AND fixed_interval_seconds IS NULL AND daily_times IS NULL)
 OR (schedule_kind = 'FIXED_INTERVAL' AND calendar_unit IS NULL AND interval_count IS NULL
        AND fixed_interval_seconds BETWEEN 60 AND 31536000 AND daily_times IS NULL)
 OR (schedule_kind = 'DAILY_TIMES' AND calendar_unit IS NULL AND interval_count IS NULL
        AND fixed_interval_seconds IS NULL AND CHAR_LENGTH(daily_times) > 0)
);
ALTER TABLE care_plan ADD CONSTRAINT ck_care_plan_schedule_bounds_v2 CHECK (
    (repetitions IS NULL OR repetitions > 0)
    AND (end_at_instant IS NULL OR end_at_instant >= start_at_instant)
);

