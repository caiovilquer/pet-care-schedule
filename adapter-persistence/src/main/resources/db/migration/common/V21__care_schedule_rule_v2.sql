-- ADR-002: expand the care schedule schema before retiring legacy wall-clock columns.
CREATE TABLE care_schedule_migration_issue (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    entity_type VARCHAR(32) NOT NULL,
    entity_id VARCHAR(64) NOT NULL,
    issue_code VARCHAR(64) NOT NULL,
    details VARCHAR(1000) NOT NULL,
    detected_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP
);

ALTER TABLE care_plan ADD COLUMN zone_id VARCHAR(64);
ALTER TABLE care_plan ADD COLUMN start_at_instant TIMESTAMP WITH TIME ZONE;
ALTER TABLE care_plan ADD COLUMN schedule_kind VARCHAR(32);
ALTER TABLE care_plan ADD COLUMN calendar_unit VARCHAR(16);
ALTER TABLE care_plan ADD COLUMN fixed_interval_seconds BIGINT;
ALTER TABLE care_plan ADD COLUMN daily_times VARCHAR(512);
ALTER TABLE care_plan ADD COLUMN end_at_instant TIMESTAMP WITH TIME ZONE;
ALTER TABLE care_plan ALTER COLUMN repetitions TYPE BIGINT;

UPDATE care_plan p
SET zone_id = COALESCE(h.timezone, 'America/Sao_Paulo')
FROM household h
WHERE h.id = p.household_id;

-- A round-trip mismatch identifies a local time in a DST gap. A neighboring
-- instant resolving to the same local time identifies an overlap. The
-- migration aborts instead of silently choosing a different historical slot.
INSERT INTO care_schedule_migration_issue(entity_type, entity_id, issue_code, details)
SELECT 'CARE_PLAN', p.id::text, 'START_AT_DST_GAP',
       'start_at=' || p.start_at::text || ', zone_id=' || p.zone_id
FROM care_plan p
WHERE ((p.start_at AT TIME ZONE p.zone_id) AT TIME ZONE p.zone_id) <> p.start_at;

INSERT INTO care_schedule_migration_issue(entity_type, entity_id, issue_code, details)
SELECT 'CARE_PLAN', p.id::text, 'START_AT_DST_OVERLAP',
       'start_at=' || p.start_at::text || ', zone_id=' || p.zone_id
FROM care_plan p
WHERE ((p.start_at AT TIME ZONE p.zone_id) - INTERVAL '1 hour') AT TIME ZONE p.zone_id = p.start_at
   OR ((p.start_at AT TIME ZONE p.zone_id) + INTERVAL '1 hour') AT TIME ZONE p.zone_id = p.start_at;

INSERT INTO care_schedule_migration_issue(entity_type, entity_id, issue_code, details)
SELECT 'CARE_PLAN', p.id::text, 'FINAL_DATE_DST_GAP',
       'final_date=' || p.final_date::text || ', zone_id=' || p.zone_id
FROM care_plan p
WHERE p.final_date IS NOT NULL
  AND ((p.final_date AT TIME ZONE p.zone_id) AT TIME ZONE p.zone_id) <> p.final_date;

INSERT INTO care_schedule_migration_issue(entity_type, entity_id, issue_code, details)
SELECT 'CARE_PLAN', p.id::text, 'FINAL_DATE_DST_OVERLAP',
       'final_date=' || p.final_date::text || ', zone_id=' || p.zone_id
FROM care_plan p
WHERE p.final_date IS NOT NULL
  AND (((p.final_date AT TIME ZONE p.zone_id) - INTERVAL '1 hour') AT TIME ZONE p.zone_id = p.final_date
    OR ((p.final_date AT TIME ZONE p.zone_id) + INTERVAL '1 hour') AT TIME ZONE p.zone_id = p.final_date);

UPDATE care_plan
SET start_at_instant = start_at AT TIME ZONE zone_id,
    schedule_kind = CASE WHEN frequency IS NULL THEN 'ONE_TIME' ELSE 'CALENDAR_INTERVAL' END,
    calendar_unit = CASE frequency
        WHEN 'DAILY' THEN 'DAY'
        WHEN 'WEEKLY' THEN 'WEEK'
        WHEN 'MONTHLY' THEN 'MONTH'
        WHEN 'YEARLY' THEN 'YEAR'
        ELSE NULL
    END,
    end_at_instant = CASE WHEN final_date IS NULL THEN NULL ELSE final_date AT TIME ZONE zone_id END;

