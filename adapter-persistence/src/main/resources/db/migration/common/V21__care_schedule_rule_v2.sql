INSERT INTO care_schedule_migration_issue(entity_type, entity_id, issue_code, details)
SELECT 'CARE_PLAN_REVISION', plan_id::text || ':' || schedule_revision::text, 'SEQUENCE_GAP',
       'min=' || MIN(sequence_number)::text || ', max=' || MAX(sequence_number)::text || ', count=' || COUNT(*)::text
FROM care_occurrence
GROUP BY plan_id, schedule_revision
HAVING MIN(sequence_number) <> 0 OR COUNT(*) <> MAX(sequence_number) - MIN(sequence_number) + 1;

DO $$
DECLARE issue_summary TEXT;
BEGIN
    SELECT string_agg(entity_type || ':' || entity_id || ':' || issue_code, ', ' ORDER BY id)
    INTO issue_summary
    FROM care_schedule_migration_issue;
    IF issue_summary IS NOT NULL THEN
        RAISE EXCEPTION 'care schedule v2 backfill refused: %', issue_summary;
    END IF;
END $$;

CREATE TABLE care_plan_materialization_cursor (
    plan_id UUID NOT NULL,
    schedule_revision INTEGER NOT NULL,
    next_sequence BIGINT,
    next_due_at_instant TIMESTAMP WITH TIME ZONE,
    materialized_through_instant TIMESTAMP WITH TIME ZONE,
    status VARCHAR(16) NOT NULL,
    version BIGINT NOT NULL DEFAULT 0,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL,
    PRIMARY KEY (plan_id, schedule_revision),
    CONSTRAINT fk_care_cursor_plan FOREIGN KEY (plan_id) REFERENCES care_plan(id) ON DELETE CASCADE,
    CONSTRAINT ck_care_cursor_status CHECK (status IN ('ACTIVE', 'EXHAUSTED', 'SUPERSEDED')),
    CONSTRAINT ck_care_cursor_next_slot CHECK (
        (status = 'ACTIVE' AND next_sequence IS NOT NULL AND next_sequence >= 0 AND next_due_at_instant IS NOT NULL)
        OR (status <> 'ACTIVE' AND next_sequence IS NULL AND next_due_at_instant IS NULL)
    ),
    CONSTRAINT ck_care_cursor_progress CHECK (
        materialized_through_instant IS NULL OR next_due_at_instant IS NULL
        OR next_due_at_instant > materialized_through_instant
    )
);

WITH materialized AS (
    SELECT p.id AS plan_id, p.schedule_revision, p.schedule_kind, p.calendar_unit,
           p.interval_count, p.repetitions, p.end_at_instant, p.zone_id, p.start_at_instant,
           COALESCE(MAX(o.sequence_number), -1) + 1 AS next_sequence,
           MAX(o.due_at_instant) AS materialized_through
    FROM care_plan p
    LEFT JOIN care_occurrence o ON o.plan_id = p.id AND o.schedule_revision = p.schedule_revision
    WHERE p.active = TRUE
    GROUP BY p.id, p.schedule_revision, p.schedule_kind, p.calendar_unit, p.interval_count,
             p.repetitions, p.end_at_instant, p.zone_id, p.start_at_instant
), candidates AS (
    SELECT m.*,
           CASE
               WHEN materialized_through IS NULL THEN start_at_instant
               WHEN schedule_kind = 'ONE_TIME' THEN NULL
               WHEN calendar_unit = 'DAY' THEN
                   ((materialized_through AT TIME ZONE zone_id) + make_interval(days => interval_count::int)) AT TIME ZONE zone_id
               WHEN calendar_unit = 'WEEK' THEN
                   ((materialized_through AT TIME ZONE zone_id) + make_interval(days => (interval_count * 7)::int)) AT TIME ZONE zone_id
               WHEN calendar_unit = 'MONTH' THEN
                   ((materialized_through AT TIME ZONE zone_id) + make_interval(months => interval_count::int)) AT TIME ZONE zone_id
               WHEN calendar_unit = 'YEAR' THEN
                   ((materialized_through AT TIME ZONE zone_id) + make_interval(years => interval_count::int)) AT TIME ZONE zone_id
               ELSE NULL
           END AS candidate_due_at
    FROM materialized m
), bounded AS (
    SELECT c.*,
           CASE
               WHEN candidate_due_at IS NULL THEN NULL
               WHEN repetitions IS NOT NULL AND next_sequence >= repetitions THEN NULL
               WHEN end_at_instant IS NOT NULL AND candidate_due_at > end_at_instant THEN NULL
               ELSE candidate_due_at
           END AS next_due_at
    FROM candidates c
)
INSERT INTO care_plan_materialization_cursor(
    plan_id, schedule_revision, next_sequence, next_due_at_instant,
    materialized_through_instant, status, updated_at
)
SELECT plan_id, schedule_revision,
       CASE WHEN next_due_at IS NULL THEN NULL ELSE next_sequence END,
       next_due_at, materialized_through,
       CASE WHEN next_due_at IS NULL THEN 'EXHAUSTED' ELSE 'ACTIVE' END,
       CURRENT_TIMESTAMP
FROM bounded;

